package com.codefirstgirls.week1.session1;

public class Exercise3 {
    public static void run() {
        int yourAge = 30;
        System.out.println("Your age: " + yourAge);

        String timePassed = "1 year later";
        System.out.println("Time passed: " + timePassed);

        yourAge++;
        System.out.println("Your age after 1 year: " + yourAge);

        timePassed = "2 years later";
        System.out.println("Time passed updated: " + timePassed);
    }

}

